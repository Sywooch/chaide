        	<h1>7.- ¿Por qué la almohada es un elemento muy importante para el descanso?</h1>
            <div class="resp-pre">
            	La almohada es un elemento importante porque sostiene la cabeza y ofrece un apoyo al cuello, que debe mantener una forma armónica y alineada en relación al resto de la columna vertebral. De esta forma al momento de adquirir una almohada, la elección no debe realizarse a la ligera. Se debe escoger una almohada que se acomode a la forma anatómica, rellenando el espacio que existe entre la cabeza y el hombro, de manera que nos brinde suficiente confort y soporte para mantener una alineación correcta entre la columna dorsal y la columna cervical, necesaria para el descanso de nuestros músculos y para disfrutar de un sueño reparador.
<br/><br/>
La almohada ideal no debe ser ni muy blanda, ni muy rígida, lo aconsejable es que se acomode y adapte al cuerpo, ya que todas las personas tenemos una contextura diferente. En cuanto a la altura de la almohada, se debe asegurar que esta no ocasione una flexión del cuello si la persona duerme boca arriba o una lateralización exagerada de la cabeza, ya que puede provocar contracturas musculares, lesiones, dolor en el cuello y el brazo. 
